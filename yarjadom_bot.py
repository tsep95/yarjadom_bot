async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_input = update.message.text.strip()

    # –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ —Ä–µ–∂–∏–º–∞
    if user_input.lower() == "–ø–æ–Ω—è—Ç—å —Å–µ–±—è":
        context.user_data.clear()
        context.user_data["history"] = [{"role": "system", "content": SYSTEM_PROMPT}]
        context.user_data["mode"] = "analyze"
        await update.message.reply_text("–•–æ—Ä–æ—à–æ üåø –î–∞–≤–∞–π –ø–æ–ø—Ä–æ–±—É–µ–º –º—è–≥–∫–æ —Ä–∞–∑–æ–±—Ä–∞—Ç—å—Å—è, —á—Ç–æ —Ç–µ–±—è —Ç—Ä–µ–≤–æ–∂–∏—Ç. –û—Ç–≤–µ—Ç—å, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –Ω–∞ –º–æ–π –≤–æ–ø—Ä–æ—Å: \n\n–≠—Ç–æ –±–æ–ª—å—à–µ —Å–≤—è–∑–∞–Ω–æ —Å —ç–º–æ—Ü–∏—è–º–∏?")
        return

    if user_input.lower() == "–±–µ—Å–µ–¥–∞":
        context.user_data.clear()
        context.user_data["history"] = [{"role": "system", "content": SYSTEM_PROMPT}]
        context.user_data["mode"] = "chat"
        await update.message.reply_text("–ü–µ—Ä–µ–∫–ª—é—á–∞—é—Å—å –≤ —Ä–µ–∂–∏–º –±–µ—Å–µ–¥—ã üí¨ –ú–æ–∂–µ—à—å –ø—Ä–æ—Å—Ç–æ —Ä–∞—Å—Å–∫–∞–∑–∞—Ç—å, –∫–∞–∫ —Ç—ã —Å–µ–±—è —á—É–≤—Å—Ç–≤—É–µ—à—å üòä")

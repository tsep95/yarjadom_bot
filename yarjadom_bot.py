    text = re.sub(r'–ï—Å–ª–∏ —Ö–æ—á–µ—à—å –≥–ª—É–±–∂–µ —Ä–∞–∑–æ–±—Ä–∞—Ç—å—Å—è, –ø–µ—Ä–µ—Ö–æ–¥–∏ –≤ \*—Ä–∞—Å—à–∏—Ä–µ–Ω–Ω—É—é –≤–µ—Ä—Å–∏—é\* üöÄ\..*?üåà\.', '', text, flags=re.DOTALL)


    emoji_list = ["üòä", "ü§ó", "üíõ", "üåø", "üíå", "üòå", "üå∏", "‚ú®", "‚òÄÔ∏è", "üåü"]
    if any(emoji in gpt_response for emoji in emoji_list):
        for emoji in emoji_list:
            if emoji in gpt_response:
                user_data[user_id]["last_emoji"] = emoji
                break

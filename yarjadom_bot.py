def add_emojis(text):
    sentences = re.split(r'(?<=[.!?])\s+', text.strip())
    result = []
    total_emojis = 1
    
    for sentence in sentences:
        words = sentence.split()
        length = len(words)
        
        if total_emojis >= 3:  # –õ–∏–º–∏—Ç 3 —ç–º–æ–¥–∑–∏ –¥–ª—è –≤—Å–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
            result.append(sentence)
            continue
            
        if length <= 6 and total_emojis < 3:  # –ö–æ—Ä–æ—Ç–∫–æ–µ –∏ —Å—Ä–µ–¥–Ω–µ–µ ‚Äî 1 —ç–º–æ–¥–∑–∏
            sentence += f" {random.choice(EMOJI_LIST)}"
            total_emojis += 1
        elif length > 6 and total_emojis <= 1:  # –î–ª–∏–Ω–Ω–æ–µ ‚Äî –¥–æ 2 —ç–º–æ–¥–∑–∏, –µ—Å–ª–∏ –µ—â—ë –µ—Å—Ç—å –º–µ—Å—Ç–æ
            half = len(words) // 2
            sentence = " ".join(words[:half]) + f" {random.choice(EMOJI_LIST)} " + " ".join(words[half:]) + f" {random.choice(EMOJI_LIST)}"
            total_emojis += 2
        result.append(sentence)
    
    return "\n".join(result)


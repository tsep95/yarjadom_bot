async def send_long_message(chat_id: int, text: str, context: ContextTypes.DEFAULT_TYPE) -> None:
    MAX_LENGTH = 4096
    for i in range(0, len(text), MAX_LENGTH):
        await context.bot.send_message(chat_id=chat_id, text=text[i:i + MAX_LENGTH])
        await asyncio.sleep(0.3)

# –§—É–Ω–∫—Ü–∏—è –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –¥–∏–∞–ª–æ–≥–∞
async def finish_conversation(user_id: int, emotion: str, context: ContextTypes.DEFAULT_TYPE, state: dict):
    therapy = THERAPY_METHODS.get(emotion, THERAPY_METHODS["–Ω–µ–æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–æ—Å—Ç—å"])
    final_response = FINAL_MESSAGE.format(cause=emotion, method=therapy[0], reason=therapy[1])
    thinking_msg_id = state.get("thinking_msg_id")
    if thinking_msg_id:
        try:
            await context.bot.delete_message(chat_id=user_id, message_id=thinking_msg_id)
        except Exception as e:
            logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ {thinking_msg_id}: {str(e)}")
    await context.bot.send_message(
        chat_id=user_id,
        text=final_response,
        reply_markup=create_more_info_keyboard()
    )
    logger.info(f"–î–∏–∞–ª–æ–≥ –∑–∞–≤–µ—Ä—à–µ–Ω. –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {user_id}, –≠–º–æ—Ü–∏—è: {emotion}")
    del user_data[user_id]

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –æ–¥–∏–Ω–∞–∫–æ–≤—ã—Ö —ç–º–æ—Ü–∏–π
def check_emotion_confidence(emotion_history: list) -> tuple:
    if not emotion_history:
        return None, 0
    emotion_counts = {}
    for emotion in emotion_history:
        emotion_counts[emotion] = emotion_counts.get(emotion, 0) + 1
    dominant_emotion = max(emotion_counts, key=emotion_counts.get)
    count = emotion_counts[dominant_emotion]
    return dominant_emotion, count

# –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–æ–±—â–µ–Ω–∏–π
async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    user_id = update.effective_chat.id
    user_input = update.message.text
    
    if user_id not in user_data:
        await start(update, context)
        return
    
    state = user_data[user_id]
    if not state["started"]:
        return  # –ñ–¥—ë–º –Ω–∞–∂–∞—Ç–∏—è –∫–Ω–æ–ø–∫–∏ "–ü—Ä–∏—Å—Ç—É–ø–∏–º"

    state["history"].append({"role": "user", "content": user_input})
    if len(state["history"]) > 30:  # –•—Ä–∞–Ω–∏–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ 15 –ø–∞—Ä –≤–æ–ø—Ä–æ—Å-–æ—Ç–≤–µ—Ç
        state["history"] = state["history"][-30:]
    state["question_count"] += 1
    
    thinking_msg = await update.message.reply_text("–î—É–º–∞—é –Ω–∞–¥ —ç—Ç–∏–º... üåø")
    state["thinking_msg_id"] = thinking_msg.message_id
    
    try:
        messages = [{"role": "system", "content": SYSTEM_PROMPT}] + state["history"]
        completion = client.chat.completions.create(
            model="deepseek-chat",
            messages=messages,
            temperature=0.7,
            timeout=30
        )
        response = completion.choices[0].message.content
        
        logger.info(f"DeepSeek response for user {user_id}: {response}")
        
        # –£–±–∏—Ä–∞–µ–º [emotion:—ç–º–æ—Ü–∏—è] –∏ —Å–∫–æ–±–∫–∏
        clean_response = re.sub(r'\[emotion:[^\]]+\]', '', response)
        clean_response = re.sub(r'\(.*?\)', '', clean_response).strip()
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ñ–æ—Ä–º–∞—Ç (2 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è)
        sentences = clean_response.split('\n\n')
        if len(sentences) != 2:
            clean_response = "–ü–æ–Ω–∏–º–∞—é, –∫–∞–∫ –Ω–µ–ø—Ä–æ—Å—Ç–æ —Ç–µ–±–µ —Å–µ–π—á–∞—Å.\n\n–ß—É–≤—Å—Ç–≤—É–µ—à—å –ª–∏ —Ç—ã —É—Å—Ç–∞–ª–æ—Å—Ç—å –∏–ª–∏ —Ä–∞–∑–¥—Ä–∞–∂–µ–Ω–∏–µ –∏–∑-–∑–∞ —ç—Ç–æ–≥–æ? üå±"
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º —ç–º–æ—Ü–∏—é –æ—Ç DeepSeek
        emotion_match = re.search(r'\[emotion:([^\]]+)\]', response)
        detected_emotion = emotion_match.group(1) if emotion_match else "–Ω–µ–æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–æ—Å—Ç—å"
        state["emotion_history"].append(detected_emotion)
        
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç–∏
        dominant_emotion, emotion_count = check_emotion_confidence(state["emotion_history"])
        
        logger.info(f"User {user_id} - Detected emotion: {detected_emotion}, Dominant: {dominant_emotion}, Count: {emotion_count}")
        
        # –£—Å–ª–æ–≤–∏—è –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
        if emotion_count >= MIN_CONFIRMATION_QUESTIONS and dominant_emotion != "–Ω–µ–æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–æ—Å—Ç—å":
            await finish_conversation(user_id, dominant_emotion, context, state)
        elif state["question_count"] >= state["max_questions"]:
            # –ï—Å–ª–∏ –¥–æ—Å—Ç–∏–≥–Ω—É—Ç –ª–∏–º–∏—Ç –≤ 15 –≤–æ–ø—Ä–æ—Å–æ–≤, –±–µ—Ä—ë–º —ç–º–æ—Ü–∏—é —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º –æ—á–∫–æ–≤
            final_emotion = dominant_emotion if dominant_emotion else "–Ω–µ–æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–æ—Å—Ç—å"
            await finish_conversation(user_id, final_emotion, context, state)
        else:
            state["history"].append({"role": "assistant", "content": clean_response})
            await context.bot.delete_message(chat_id=user_id, message_id=thinking_msg.message_id)
            await send_long_message(user_id, clean_response, context)
        
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –≤ handle_message –¥–ª—è user_id {user_id}: {str(e)}")
        response = "–ß—Ç–æ-—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫, –∏ –º–Ω–µ –∂–∞–ª—å, —á—Ç–æ —Ç–∞–∫ –≤—ã—à–ª–æ.\n\n–•–æ—á–µ—à—å –ª–∏ —Ç—ã –ø—Ä–æ–¥–æ–ª–∂–∏—Ç—å –∏–ª–∏ –≤–∑—è—Ç—å –ø–∞—É–∑—É? üåø"
        try:
            await context.bot.delete_message(chat_id=user_id, message_id=thinking_msg.message_id)
        except Exception as delete_error:
            logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ {thinking_msg.message_id}: {str(delete_error)}")
        await send_long_message(user_id, response, context)


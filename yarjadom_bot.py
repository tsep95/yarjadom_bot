def escape_markdown_with_bold(text: str) -> str:
    """
    –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –≤—ã–¥–µ–ª–µ–Ω–∏–µ –≤–∞–∂–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞, –∑–∞–ø–∏—Å–∞–Ω–Ω–æ–≥–æ –∫–∞–∫ *—Ç–µ–∫—Å—Ç*,
    –≤ MarkdownV2 —Ñ–æ—Ä–º–∞—Ç –∂–∏—Ä–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞ —Å –¥–≤–æ–π–Ω—ã–º–∏ –∑–≤—ë–∑–¥–æ—á–∫–∞–º–∏ **—Ç–µ–∫—Å—Ç**,
    –∑–∞—Ç–µ–º —ç–∫—Ä–∞–Ω–∏—Ä—É–µ—Ç –≤—Å–µ —Å–ø–µ—Ü—Å–∏–º–≤–æ–ª—ã –¥–ª—è MarkdownV2, –∑–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ–º —Å–∏–º–≤–æ–ª–æ–≤ –∂–∏—Ä–Ω–æ–≥–æ –≤—ã–¥–µ–ª–µ–Ω–∏—è.
    """
    # –°–Ω–∞—á–∞–ª–∞ –∑–∞–º–µ–Ω—è–µ–º *text* –Ω–∞ **text**
    text = re.sub(r'\*(.*?)\*', r'**\1**', text)
    
    # –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –ø–æ –±–ª–æ–∫–∞–º, –∑–∞–∫–ª—é—á—ë–Ω–Ω—ã–º –≤ **, —á—Ç–æ–±—ã –Ω–µ —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞—Ç—å –º–∞—Ä–∫–µ—Ä—ã –∂–∏—Ä–Ω–æ—Å—Ç–∏
    segments = re.split(r'(\*\*.*?\*\*)', text)
    
    # –°–ø–∏—Å–æ–∫ —Å–ø–µ—Ü—Å–∏–º–≤–æ–ª–æ–≤ –¥–ª—è —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏—è (–∏—Å–∫–ª—é—á–∞–µ–º *, —Ç–∞–∫ –∫–∞–∫ –æ–Ω –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤ **bold**)
    reserved_chars = r'_ \[\]\(\)~`>#\+-=|{}\.\!\\'
    # –û–±—Ä–∞—Ç–∏—Ç–µ –≤–Ω–∏–º–∞–Ω–∏–µ: –ø—Ä–æ–±–µ–ª –ø–æ—Å–ª–µ _ –¥–æ–±–∞–≤–ª–µ–Ω –¥–ª—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç–∏ —à–∞–±–ª–æ–Ω–∞
    # –≠–∫—Ä–∞–Ω–∏—Ä—É–µ–º —Å–∏–º–≤–æ–ª—ã —Ç–æ–ª—å–∫–æ –≤ —Å–µ–≥–º–µ–Ω—Ç–∞—Ö, –Ω–µ —è–≤–ª—è—é—â–∏—Ö—Å—è –∂–∏—Ä–Ω—ã–º–∏ –±–ª–æ–∫–∞–º–∏
    escaped_segments = []
    for segment in segments:
        if segment.startswith("**") and segment.endswith("**"):
            # –û—Å—Ç–∞–≤–ª—è–µ–º –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π
            escaped_segments.append(segment)
        else:
            seg_escaped = re.sub(rf'([{reserved_chars}])', r'\\\1', segment)
            escaped_segments.append(seg_escaped)
    
    return ''.join(escaped_segments)

